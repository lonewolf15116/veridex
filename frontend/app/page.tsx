const SEV_ORDER: Severity[] = ["critical", "high", "medium", "low"];

// ─────────────────────────────────────────────
// Small helpers
// ─────────────────────────────────────────────

function sevRank(s: Severity) { return SEV_ORDER.indexOf(s); }

