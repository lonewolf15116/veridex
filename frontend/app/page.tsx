function LensPanel({ lens, state }: { lens: typeof LENSES[number]; state: LensState }) {
  const [open, setOpen] = useState(false);
  const isDone    = state.status === "done";
  const isRunning = state.status === "running";
  const isError   = state.status === "error";
  const isIdle    = state.status === "idle";

  const critCount = isDone ? state.result.flaws.filter(f => f.severity === "critical").length : 0;
  const highCount = isDone ? state.result.flaws.filter(f => f.severity === "high").length : 0;
  const total     = isDone ? state.result.flaws.length : 0;

  return (
    <div
      className={`rounded-2xl border transition-all duration-200 ${
        isDone    ? "border-white/10 bg-[#111] cursor-pointer hover:border-white/[0.15]"
        : isRunning ? "border-white/10 bg-[#0f0f0f]"
        : isError   ? "border-red-500/20 bg-[#0f0f0f]"
        : "border-white/[0.06] bg-[#0c0c0c]"
      }`}
      onClick={() => isDone && setOpen(o => !o)}
    >
      {/* Header row */}
      <div className="flex items-center justify-between px-6 py-5">
        <div className="flex items-center gap-4">
          <div className="flex items-center justify-center w-8 h-8 rounded-lg bg-white/[0.04] border border-white/[0.07] shrink-0">
            {isRunning ? <span className="w-1.5 h-1.5 rounded-full bg-white/50 animate-pulse" />
              : isDone   ? <span className="w-1.5 h-1.5 rounded-full bg-white/60" />
              : isError  ? <span className="w-1.5 h-1.5 rounded-full bg-red-400" />
              : <span className="w-1.5 h-1.5 rounded-full bg-white/15" />}
          </div>
          <div>
            <Tag>{lens.tag}</Tag>
            <p className={`font-semibold text-[15px] leading-tight mt-0.5 ${isIdle ? "text-white/30" : "text-white/90"}`}>
              {lens.label}
            </p>
            <p className={`text-[11px] mt-0.5 ${isIdle ? "text-white/15" : "text-white/35"}`}>
              {lens.description}
            </p>
          </div>
        </div>

        <div className="flex items-center gap-2 shrink-0">
          {isRunning && <span className="text-[11px] text-white/30 italic">Analyzing…</span>}
          {isError   && <span className="text-[11px] text-red-400/70">Failed</span>}
          {isDone && (
            <>
              {critCount > 0 && <span className="rounded-full bg-red-400/10 border border-red-400/20 px-2.5 py-1 text-[10px] font-bold uppercase tracking-wide text-red-400">{critCount} critical</span>}
              {highCount > 0 && <span className="rounded-full bg-orange-400/10 border border-orange-400/20 px-2.5 py-1 text-[10px] font-bold uppercase tracking-wide text-orange-400">{highCount} high</span>}
              <span className="text-[11px] text-white/25 font-mono ml-1">{total} flaws</span>
              <span className="text-white/20 text-[13px] ml-2 select-none">{open ? "↑" : "↓"}</span>
            </>
          )}
        </div>
      </div>

      {/* Expanded */}
      {isDone && open && (
        <>
          <Divider />
          <div className="px-6 py-5 border-b border-white/[0.06]">
            <Tag>Summary</Tag>
            <p className="mt-2 text-[13px] text-white/55 leading-relaxed">{state.result.summary}</p>
          </div>
          <div className="px-6 py-5 space-y-3">
            <Tag>Findings</Tag>
            <div className="space-y-3 mt-2">
              {[...state.result.flaws]
                .sort((a, b) => sevRank(a.severity) - sevRank(b.severity))
                .map((flaw, i) => <FlawCard key={i} flaw={flaw} />)}
            </div>
          </div>
        </>
      )}

      {isError && "reason" in state && (
        <>
          <Divider />
          <div className="px-6 py-4">
            <p className="text-[11px] text-red-400/60">{state.reason}</p>
          </div>
        </>
      )}
    </div>
  );
}

