// ─────────────────────────────────────────────
// Types
// ─────────────────────────────────────────────

type Severity = "low" | "medium" | "high" | "critical";

interface Flaw {
  title: string;
  severity: Severity;
  description: string;
  question: string;
}

interface CriticPassResult {
  lens: string;
  summary: string;
  flaws: Flaw[];
}

type LensState =
  | { status: "idle" }
  | { status: "running" }
  | { status: "done"; result: CriticPassResult }
  | { status: "error"; reason: string };

type LensId =
  | "pre_mortem"
  | "unit_economics"
  | "adversarial_competitor"
  | "execution_risk";

// ─────────────────────────────────────────────
// Export helpers
// ─────────────────────────────────────────────

function critiqueToPlainText(
  lensStates: Record<LensId, LensState>,
  lenses: typeof LENSES
): string {
  const lines: string[] = ["VERIDEX CRITIQUE", "═".repeat(60), ""];
  for (const lens of lenses) {
    const state = lensStates[lens.id];
    if (state.status !== "done") continue;
    const r = state.result;
    lines.push(`${lens.tag} · ${lens.label.toUpperCase()}`);
    lines.push("─".repeat(40));
    lines.push(r.summary);
    lines.push("");
    for (const flaw of r.flaws) {
      lines.push(`[${flaw.severity.toUpperCase()}] ${flaw.title}`);
      lines.push(flaw.description);
      lines.push(`→ ${flaw.question}`);
      lines.push("");
    }
    lines.push("");
  }
  lines.push(`Generated by Veridex · veridex.fyi`);
  return lines.join("\n");
}

function critiqueToMarkdown(
  lensStates: Record<LensId, LensState>,
  lenses: typeof LENSES
): string {
  const lines: string[] = ["# Veridex Critique", ""];
  for (const lens of lenses) {
    const state = lensStates[lens.id];
    if (state.status !== "done") continue;
    const r = state.result;
    lines.push(`## ${lens.label}`);
    lines.push(`> ${r.summary}`);
    lines.push("");
    for (const flaw of r.flaws) {
      lines.push(`### \`${flaw.severity.toUpperCase()}\` ${flaw.title}`);
      lines.push(flaw.description);
      lines.push("");
      lines.push(`**Key question:** ${flaw.question}`);
      lines.push("");
    }
  }
  lines.push("---");
  lines.push("*Generated by [Veridex](https://veridex.fyi)*");
  return lines.join("\n");
}

