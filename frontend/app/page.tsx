// Export bar
// ─────────────────────────────────────────────

function ExportBar({ lensStates }: { lensStates: Record<LensId, LensState> }) {
  const [copied, setCopied] = useState(false);

  const handleCopy = useCallback(async () => {
    const text = critiqueToPlainText(lensStates, LENSES);
    try {
      await navigator.clipboard.writeText(text);
      setCopied(true);
      setTimeout(() => setCopied(false), 2000);
    } catch {
      // Fallback for browsers that block clipboard without HTTPS
      const el = document.createElement("textarea");
      el.value = text;
      el.style.position = "fixed";
      el.style.opacity = "0";
      document.body.appendChild(el);
      el.select();
      document.execCommand("copy");
      document.body.removeChild(el);
      setCopied(true);
      setTimeout(() => setCopied(false), 2000);
    }
  }, [lensStates]);

  const handleDownload = useCallback(() => {
    const md = critiqueToMarkdown(lensStates, LENSES);
    const blob = new Blob([md], { type: "text/markdown;charset=utf-8" });
    const url = URL.createObjectURL(blob);
    const a = document.createElement("a");
    a.href = url;
    a.download = `veridex-critique-${new Date().toISOString().slice(0, 10)}.md`;
    a.click();
    URL.revokeObjectURL(url);
  }, [lensStates]);

  return (
    <div className="flex items-center justify-between rounded-2xl border border-white/[0.06] bg-[#0f0f0f] px-6 py-4">
      <div>
        <p className="text-[12px] font-semibold text-white/60">Export critique</p>
        <p className="text-[11px] text-white/25 mt-0.5">Copy to clipboard or download as Markdown</p>
      </div>
      <div className="flex items-center gap-2">
        <button
          onClick={handleCopy}
          className="flex items-center gap-2 rounded-xl border border-white/10 bg-white/[0.04] px-4 py-2 text-[12px] font-semibold text-white/70 transition-all hover:bg-white/[0.07] hover:text-white active:scale-[0.97]"
        >
          {copied ? (
            <><span className="text-green-400">✓</span> Copied</>
          ) : (
            <><span className="text-white/30">⎘</span> Copy</>
          )}
        </button>
        <button
          onClick={handleDownload}
          className="flex items-center gap-2 rounded-xl border border-white/10 bg-white/[0.04] px-4 py-2 text-[12px] font-semibold text-white/70 transition-all hover:bg-white/[0.07] hover:text-white active:scale-[0.97]"
        >
          <span className="text-white/30">↓</span> Download .md
        </button>
      </div>
    </div>
  );
}

